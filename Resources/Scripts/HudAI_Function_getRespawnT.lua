--------------------------------------------------------------------------------
function HudAI.getRespawnT ( )
--------------------------------------------------------------------------------
	
    local x, y, z
    
    --sets translation values
    x = hashtable.get ( this.htRespawn ( ), "xT" )
    y = hashtable.get ( this.htRespawn ( ), "yT" )
    z = hashtable.get ( this.htRespawn ( ), "zT" )
	
    --returns values
	return x, y, z
--------------------------------------------------------------------------------
end
