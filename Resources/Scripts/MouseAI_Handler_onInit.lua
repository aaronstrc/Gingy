function MouseAI.onInit (  )
--------------------------------------------------------------------------------
	
    --sets the avatar object
    this.hMouse ( this.getObject ( ) )
    
    --calls the a function to set the animations modes
    if(this.hMouse ( ) ~= nil) then
        this.setAnimations ( )
        this.setIdle ( )
        
    end
    
--------------------------------------------------------------------------------
end
