function WolfAI.Run_onEnter ( )
--------------------------------------------------------------------------------
	
	--sets animation
    this.setRun ( )
	
    --adds dynamics
    dynamics.setLinearVelocity ( this.hEnemy( ), 0, 0, 13, object.kLocalSpace )
    dynamics.addLinearImpulse ( this.hEnemy( ), 0, 0, 13, object.kLocalSpace )
	
--------------------------------------------------------------------------------
end
