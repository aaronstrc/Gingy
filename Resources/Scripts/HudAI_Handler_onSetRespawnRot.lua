--------------------------------------------------------------------------------
--  Handler.......... : onSetRespawnRot
--  Author........... : Aaron Stricklin
--  Description...... : Resets respawn rotation
--------------------------------------------------------------------------------

--------------------------------------------------------------------------------
function HudAI.onSetRespawnRot ( x, y, z  )
--------------------------------------------------------------------------------
	
	--sets the respawn value
    hashtable.set ( this.htRespawn ( ), "xR", x)
    hashtable.set ( this.htRespawn ( ), "yR", y)
    hashtable.set ( this.htRespawn ( ), "zR", z)
	
--------------------------------------------------------------------------------
end
