function WolfAI.Jump_onEnter ( )
--------------------------------------------------------------------------------
	
    --sets animation
    this.setJump ( )
	
    --adds dynamics
    dynamics.setLinearVelocity ( this.hEnemy( ), 0, 10, 13, object.kLocalSpace )
    dynamics.addLinearImpulse ( this.hEnemy( ), 0, 10, 13, object.kLocalSpace )
    
--------------------------------------------------------------------------------
end
