--------------------------------------------------------------------------------
function HudAI.onSetRespawnPos ( x, y, z )
--------------------------------------------------------------------------------
	
	--sets the respawn value
    hashtable.set ( this.htRespawn ( ), "xT", x)
    hashtable.set ( this.htRespawn ( ), "yT", y)
    hashtable.set ( this.htRespawn ( ), "zT", z)
	
--------------------------------------------------------------------------------
end
