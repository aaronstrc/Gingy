--------------------------------------------------------------------------------
function HudAI.getRespawnR ( )
--------------------------------------------------------------------------------
	
	local x, y, z
    
    --sets translation values
    x = hashtable.get ( this.htRespawn ( ), "xR" )
    y = hashtable.get ( this.htRespawn ( ), "yR" )
    z = hashtable.get ( this.htRespawn ( ), "zR" )
	
    --returns values
	return x, y, z
	
--------------------------------------------------------------------------------
end
