function SpiderAI.idle_onLoop ( )
--------------------------------------------------------------------------------
	
    --if the jump sequence has ended set this.bJump ( ) to false
    if( this.bPunch ( ) == true and animation.getPlaybackCursor ( this.hSpider( ), 0 ) >= hashtable.get (this.htAnimations ( ), "punch_end"  ) - 1 ) then
        this.bPunch ( false )
        this.setIdle ( )
    end
--------------------------------------------------------------------------------
end
